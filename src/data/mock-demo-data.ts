// ── Helpers ──────────────────────────────────────────────────────────────────

function isoKR(dateStr: string, time = '12:00:00'): string {
  // dateStr: YYYY-MM-DD, returns ISO string at KST
  return `${dateStr}T${time}+09:00`;
}

function today(): string {
  const d = new Date();
  const fmt = new Intl.DateTimeFormat('en-CA', { timeZone: 'Asia/Seoul' });
  return fmt.format(d); // YYYY-MM-DD
}

function addDays(base: string, days: number): string {
  const d = new Date(base + 'T12:00:00+09:00');
  d.setDate(d.getDate() + days);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, '0');
  const day = String(d.getDate()).padStart(2, '0');
  return `${y}-${m}-${day}`;
}

