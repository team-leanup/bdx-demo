function makeRecord(
  id: string,
  designerId: string,
  customerId: string,
  scope: DesignScope,
  price: number,
  date: string,
  opts?: { bookingId?: string; isQuickSale?: boolean; paymentMethod?: 'card' | 'cash' },
): ConsultationRecord {
  const iso = isoKR(date, '12:00:00');
  return {
    id,
    shopId: SHOP_ID,
    designerId,
    customerId,
    consultation: {
      bodyPart: 'hand',
      offType: 'none',
      extensionType: 'none',
      nailShape: 'round',
      designScope: scope,
      expressions: [],
      hasParts: scope === 'full_art',
      partsSelections: [],
      extraColorCount: 0,
      currentStep: ConsultationStep.SUMMARY,
      bookingId: opts?.bookingId,
    },
    totalPrice: price,
    // time-calculator.ts 규칙 기준 (핸드 base 60 + scope 디자인 시간)
    estimatedMinutes:
      scope === 'full_art' ? 100
      : scope === 'monthly_art' ? 95
      : scope === 'solid_point' ? 80
      : 60,
    finalPrice: price,
    createdAt: iso,
    updatedAt: iso,
    finalizedAt: iso,
    paymentMethod: opts?.paymentMethod ?? 'card',
    isQuickSale: opts?.isQuickSale,
  };
}

