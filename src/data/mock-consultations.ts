function getRelativeDate(daysAgo: number): string {
  const [year, month, day] = addDaysInKorea(getTodayInKorea(), -daysAgo).split('-').map(Number);
  const d = createKoreanDate(year, month, day, 12);
  return d.toISOString();
}

